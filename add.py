# Simple program to add two numbers
# Input: Two numbers
# Output: Their sum

# Input from the user
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Adding the numbers
sum = num1 + num2

# Displaying the result
print(f"The sum of {num1} and {num2} is: {sum}")
